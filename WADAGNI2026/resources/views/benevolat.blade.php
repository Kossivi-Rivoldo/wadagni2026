// =====================================================
// ANIMATION D'APPARITION DES TEXTES
// =====================================================
const animatedTexts = document.querySelectorAll("h2, p, label");

animatedTexts.forEach((el, index) => {
    el.style.opacity = "0";
    el.style.transform = "translateY(20px)";
    setTimeout(() => {
        el.style.transition = "0.6s";
        el.style.opacity = "1";
        el.style.transform = "translateY(0)";
    }, 200 * index);
});

