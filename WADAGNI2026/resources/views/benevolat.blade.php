const topBar = document.querySelector(".bg-primary.text-white.text-center.fw-bold.py-2");

setTimeout(() => {
    if (topBar) {
        topBar.style.transition = "0.6s";
        topBar.style.backgroundColor = "#004aad";
    }
}, 3000);

setTimeout(() => {
    if (topBar) {
        topBar.style.transition = "0.6s";
        topBar.style.letterSpacing = "2px";
    }
}, 4500);

// =====================================================
// ANIMATION SUR LES ICONES EN CERCLE
// =====================================================
document.querySelectorAll(".bg-info.rounded-circle").forEach(icon => {
    icon.style.transition = "0.4s";
    icon.addEventListener("mouseenter", () => {
        icon.style.transform = "scale(1.2) rotate(8deg)";
        icon.style.boxShadow = "0 0 10px rgba(0,0,0,0.3)";
    });
    icon.addEventListener("mouseleave", () => {
        icon.style.transform = "scale(1) rotate(0)";
        icon.style.boxShadow = "none";
    });
});

// =====================================================
// VALIDATION DU FORMULAIRE + INTERACTION TEMPS RÉEL
// =====================================================
const form = document.querySelector("form");
const champs = form.querySelectorAll("input, select");

// Nettoyage des espaces
function nettoyerTexte(texte) {
    return texte.replace(/\s+/g, " ").trim();
}

// Nom en MAJUSCULE
function formatNom(nom) {
    return nettoyerTexte(nom).toUpperCase();
}

// Prénom(s) avec première lettre en majuscule
function formatPrenom(prenom) {
    return nettoyerTexte(prenom)
        .toLowerCase()
        .split(" ")
        .map(mot => mot.charAt(0).toUpperCase() + mot.slice(1))
        .join(" ");
}

// Vérification email
function emailValide(email) {
    return email.includes("@");
}

// Affichage message unique
function afficherMessage(type, texte) {
    document.querySelectorAll(".alert").forEach(a => a.remove());
    form.insertAdjacentHTML(
        "beforeend",
        `<div class="alert alert-${type} mt-3 fw-bold text-center">${texte}</div>`
    );
    setTimeout(() => {
        document.querySelector(".alert")?.remove();
    }, 3500);
}

// Interaction temps réel
champs.forEach(champ => {
    champ.addEventListener("input", () => {
        champ.value = nettoyerTexte(champ.value);
        champ.style.border = "1px solid #ced4da";
    });
});

// Soumission formulaire
form.addEventListener("submit", function (e) {
    e.preventDefault();

    let valide = true;
    const nomComplet = form.querySelector("input[type='text']");
    const email = form.querySelector("input[type='email']");

    champs.forEach(champ => {
        if (
            champ.value === "" ||
            champ.value.includes("Selectionnez") ||
            champ.value.includes("Vôtre")
        ) {
            valide = false;
            champ.style.border = "2px solid red";
        }
    });

    if (!valide) {
        afficherMessage("danger", "⚠️ Veuillez renseigner tous les champs");
        return;
    }

    // Traitement Nom et Prénom
    const partiesNom = nettoyerTexte(nomComplet.value).split(" ");
    if (partiesNom.length >= 2) {
        nomComplet.value =
            formatNom(partiesNom[0]) +
            " " +
            formatPrenom(partiesNom.slice(1).join(" "));
    } else {
        nomComplet.value = formatNom(nomComplet.value);
    }

    // Vérification email
    if (!emailValide(email.value)) {
        email.style.border = "2px solid red";
        afficherMessage("danger", "❌ Email invalide");
        return;
    }

    // Succès
    afficherMessage("success", "✔️ Formulaire envoyé avec succès");

    // Nettoyage total
    champs.forEach(champ => champ.value = "");
});
</script>

